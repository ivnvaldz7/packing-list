import type { Product } from '../types';

const getLotPrefix = (code: string): string => code.replace(/\d+/g, '');
const getProductionNumber = (code: string): string => code.replace(/\D+/g, '');

const createProduct = (
  name: string,
  code: string,
  unitsPerBox: number,
  weightPerBoxKg: number,
  unit: Product['unit'] = 'Frascos',
): Product => ({
  id: `${code.toLowerCase()}-${name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`,
  code,
  name,
  lotPrefix: getLotPrefix(code),
  productionNumber: getProductionNumber(code),
  unit,
  unitsPerBox,
  weightPerBoxKg,
});

